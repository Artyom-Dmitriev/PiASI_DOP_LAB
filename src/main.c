/* ------------------------------------------------------------------ */
/*  main — точка входа.                                               */
/*                                                                    */
/*  Здесь живут два главных "состояния" программы:                    */
/*    Student *arr  — указатель на начало массива студентов;          */
/*                    NULL означает "массив пока не создан";          */
/*    size_t   n    — текущее количество элементов в массиве.         */
/*                                                                    */
/*  Все функции работы с данными будут принимать &arr и &n            */
/*  (адрес указателя и адрес размера) — чтобы уметь не только         */
/*  читать данные, но и перевыделять память (realloc) и обновлять     */
/*  размер. По-другому в C изменить значение переменной, переданной   */
/*  в функцию, нельзя — нужно передавать её адрес.                    */
/* ------------------------------------------------------------------ */
int main(void) {
    /* Под Windows переводим консоль ввода и вывода в UTF-8 (кодовая        */
    /* страница 65001), чтобы русский текст из printf и в scanf работал    */
    /* без искажений. На Linux/macOS этот блок просто отсутствует.          */
#ifdef _WIN32
    SetConsoleOutputCP(65001);
    SetConsoleCP(65001);
#endif

    Student *arr = NULL;   /* массив студентов: пока ничего не выделено   */
    size_t   n   = 0;      /* размер массива в элементах                  */

    int choice = -1;       /* выбранный пункт меню                        */

    /* Главный цикл: показываем меню, читаем выбор, выполняем действие.   */
    /* do/while гарантирует, что меню покажется хотя бы один раз.         */
    do {
        print_menu();

        if (!read_int(&choice)) {
            printf("Ошибка ввода: ожидалось число. Попробуйте снова.\n");
            choice = -1;       /* чтобы цикл не вышел случайно            */
            continue;
        }

        switch (choice) {
            case 1:
                input_students(&arr, &n);
                break;
            case 2:
                print_table(arr, n);
                break;
            case 3:
                sort_students(arr, n);
                break;
            case 4:
                find_by_group(arr, n);
                break;
            case 5:
                save_csv(arr, n);
                break;
            case 6:
                load_csv(&arr, &n);
                break;
            case 7:
                save_bin(arr, n);
                break;
            case 8:
                load_bin(&arr, &n);
                break;
            case 9:
                add_student(&arr, &n);
                break;
            case 10:
                remove_student(&arr, &n);
                break;
            case 0:
                printf("Выход. Освобождаем ресурсы...\n");
                break;
            default:
                printf("Нет такого пункта меню: %d\n", choice);
                break;
        }
    } while (choice != 0);

    /* ---- Пункт 8 задания: корректное освобождение ресурсов. ----     */
    /*                                                                  */
    /* Динамическая память: освобождаем массив студентов одним free().  */
    /*   free(NULL) безопасен — если arr и так NULL, ничего не         */
    /*   произойдёт.                                                    */
    /*                                                                  */
    /* Файлы: все функции работы с файлами (save_csv/load_csv,         */
    /*   save_bin/load_bin) сами вызывают fclose() сразу после         */
    /*   окончания работы с FILE*. Поэтому на момент выхода из main    */
    /*   открытых файлов не остаётся.                                  */
    /*                                                                  */
    /* Операционная система всё равно очистит ресурсы процесса при его  */
    /* завершении, но "корректное" освобождение — это привычка хорошего */
    /* C-программиста и явное требование задания.                      */
    free(arr);
    arr = NULL;
    n   = 0;

    return 0;
}

