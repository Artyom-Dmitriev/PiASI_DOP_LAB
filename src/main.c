/*
 * Лабораторная: база данных студентов в динамическом массиве структур.
 *
 * Программа интерактивная, управляется через меню (см. print_menu).
 * Реализованы все 8 пунктов задания:
 *   1. Ввод студентов с клавиатуры (input_students).
 *   2. Вывод таблицы (print_table).
 *   3. Сортировка по фамилии / группе / среднему баллу (sort_students + qsort).
 *   4. Поиск по номеру группы (find_by_group).
 *   5. Сохранение / чтение CSV (save_csv / load_csv).
 *   6. Сохранение / чтение бинарного файла (save_bin / load_bin).
 *   7. Добавление и удаление студента (add_student / remove_student) через realloc.
 *   8. Корректное освобождение памяти и закрытие файлов
 *      (free в конце main + fclose в каждой функции работы с файлами).
 *
 * Сборка:    gcc -Wall -Wextra -g src/main.c -o build/lab.exe
 * Запуск:    .\build\lab.exe
 */

#include <stdio.h>      /* printf, scanf, FILE, fopen, ...                    */
#include <stdlib.h>     /* malloc, calloc, realloc, free, qsort               */
#include <string.h>     /* strcmp, strcpy, memmove — пригодятся дальше        */

/* На Windows стандартная консоль cmd по умолчанию работает в кодировке     */
/* cp866 (или cp1251), а наш исходник сохранён в UTF-8. Без перевода консоли */
/* в UTF-8 русские буквы из printf выводятся "кракозябрами". Подключаем      */
/* windows.h только под Windows, в main вызываем SetConsoleOutputCP(65001).  */
#ifdef _WIN32
  #include <windows.h>
#endif

/* ------------------------------------------------------------------ */
/*  Тип данных Student — описан ровно так, как требует задание.       */
/*  typedef struct { ... } Student;  делает две вещи:                 */
/*    1) объявляет безымянную структуру с указанными полями;          */
/*    2) даёт ей короткое имя Student, чтобы дальше писать просто     */
/*       "Student s;" а не "struct Student_tag s;".                   */
/* ------------------------------------------------------------------ */
typedef struct {
    char   surname[40];   /* фамилия студента, до 39 символов + '\0'         */
    char   initials[8];   /* инициалы, например "А.Б."                       */
    int    group;         /* номер группы                                    */
    int    grades[4];     /* четыре оценки за последнюю сессию               */
    double avg_grade;     /* средний балл, считается из grades[]             */
} Student;

/* ------------------------------------------------------------------ */
/*  Утилита: безопасно прочитать целое число с клавиатуры.            */
/*                                                                    */
/*  Возвращает 1 при успехе и 0 при ошибке ввода (буква вместо цифры  */
/*  и т.п.). При ошибке "проглатывает" остаток строки, чтобы scanf    */
/*  на следующем вызове не зацикливался на одних и тех же символах.   */
/* ------------------------------------------------------------------ */
static int read_int(int *out) {
    if (scanf("%d", out) != 1) {
        /* очищаем буфер ввода до конца строки */
        int c;
        while ((c = getchar()) != '\n' && c != EOF) { /* пусто */ }
        return 0;
    }
    return 1;
}

/* ------------------------------------------------------------------ */
/*  Утилита: "съесть" остаток текущей строки из stdin.                */
/*  Нужна после scanf("%d", ...), потому что scanf оставляет '\n'     */
/*  в буфере. Если сразу за этим прочитать строку через fgets, она    */
/*  получит пустую строку (один \n) и всё сломается.                  */
/* ------------------------------------------------------------------ */
static void flush_line(void) {
    int c;
    while ((c = getchar()) != '\n' && c != EOF) { /* пусто */ }
}

/* ------------------------------------------------------------------ */
/*  Утилита: прочитать строку с клавиатуры в буфер фиксированного     */
/*  размера. Убирает завершающий '\n'. Возвращает 1 при успехе.       */
/*                                                                    */
/*  Используем fgets, а не scanf("%s"), чтобы корректно читать строки */
/*  с пробелами (фамилия из двух слов, инициалы и т.п.) и чтобы       */
/*  гарантированно не вылезти за размер буфера.                       */
/* ------------------------------------------------------------------ */
static int read_line(char *buf, size_t size) {
    if (fgets(buf, (int)size, stdin) == NULL) return 0;
    /* fgets оставляет '\n' в конце, если строка влезла. Срезаем его.  */
    size_t len = strlen(buf);
    if (len > 0 && buf[len - 1] == '\n') buf[len - 1] = '\0';
    return 1;
}

/* ------------------------------------------------------------------ */
/*  Посчитать средний балл для одного студента по его четырём оценкам.*/
/*  Кладём результат прямо в поле avg_grade.                          */
/* ------------------------------------------------------------------ */
static void compute_avg(Student *s) {
    int sum = 0;
    for (int i = 0; i < 4; ++i) sum += s->grades[i];
    s->avg_grade = sum / 4.0;   /* 4.0 — чтобы деление было дробным   */
}

/* ------------------------------------------------------------------ */
/*  Пункт 1 задания: ввод студентов с клавиатуры.                     */
/*                                                                    */
/*  Параметры — указатели на указатель и на размер, чтобы функция     */
/*  могла перезаписать сам массив (выделить новый блок памяти) и его  */
/*  размер. Сначала старый массив освобождаем — пользователь, выбрав  */
/*  пункт 1 повторно, ожидает "начать заново".                        */
/* ------------------------------------------------------------------ */
static void input_students(Student **arr, size_t *n) {
    /* Очищаем то, что было раньше. free(NULL) безопасен.              */
    free(*arr);
    *arr = NULL;
    *n   = 0;

    int count = 0;
    printf("Сколько студентов ввести? ");
    if (!read_int(&count) || count <= 0) {
        printf("Некорректное количество.\n");
        return;
    }
    flush_line();   /* убираем '\n' после числа, иначе fgets ниже сорвётся */

    /* calloc(count, sizeof(Student)) — выделяет count*sizeof(Student) */
    /* байт и сразу обнуляет их. Удобно: все поля будут в нуле, пока  */
    /* мы их не заполним.                                              */
    Student *buf = calloc((size_t)count, sizeof(Student));
    if (buf == NULL) {
        /* Если памяти нет — calloc вернёт NULL. На современных ПК это */
        /* почти невозможно, но в C проверка результата выделения —    */
        /* обязательная привычка.                                      */
        printf("Не удалось выделить память на %d студентов.\n", count);
        return;
    }

    for (int i = 0; i < count; ++i) {
        printf("\n--- Студент №%d ---\n", i + 1);

        printf("Фамилия: ");
        read_line(buf[i].surname, sizeof(buf[i].surname));

        printf("Инициалы (например, А.Б.): ");
        read_line(buf[i].initials, sizeof(buf[i].initials));

        printf("Номер группы: ");
        if (!read_int(&buf[i].group)) {
            printf("Ошибка ввода группы, ставим 0.\n");
            buf[i].group = 0;
        }

        printf("Оценки (4 числа через пробел, например: 5 4 5 4): ");
        for (int k = 0; k < 4; ++k) {
            if (!read_int(&buf[i].grades[k])) {
                printf("Ошибка ввода оценки №%d, ставим 0.\n", k + 1);
                buf[i].grades[k] = 0;
            }
        }
        flush_line();   /* очистка остатка строки после последней оценки */

        compute_avg(&buf[i]);
    }

    /* Передаём новый массив и его размер обратно в main.              */
    *arr = buf;
    *n   = (size_t)count;
    printf("\nГотово. Введено студентов: %zu\n", *n);
}

/* ------------------------------------------------------------------ */
/*  Пункт 2 задания: вывести таблицу студентов.                       */
/*                                                                    */
/*  arr и n принимаем "по значению" (а не через указатель) — функция  */
/*  только читает данные, ничего не меняет. const сигналит компилятору*/
/*  "не дам случайно изменить элемент массива".                       */
/*                                                                    */
/*  Форматирование таблицы делается через спецификаторы ширины в      */
/*  printf:  "%-20s" = левое выравнивание, ширина 20 символов;        */
/*           "%6d"   = правое выравнивание, ширина 6;                 */
/*           "%6.2f" = вещ. число, ширина 6, 2 знака после точки.     */
/* ------------------------------------------------------------------ */
static void print_table(const Student *arr, size_t n) {
    if (n == 0 || arr == NULL) {
        printf("Список пуст. Сначала введите данные (пункт 1) или загрузите из файла.\n");
        return;
    }

    printf("\n");
    printf("%-3s %-20s %-8s %-7s %-15s %-9s\n",
           "№", "Фамилия", "И.О.", "Группа", "Оценки", "Сред.балл");
    printf("--------------------------------------------------------------------\n");

    for (size_t i = 0; i < n; ++i) {
        printf("%-3zu %-20s %-8s %-7d %d %d %d %d   %6.2f\n",
               i + 1,
               arr[i].surname,
               arr[i].initials,
               arr[i].group,
               arr[i].grades[0], arr[i].grades[1],
               arr[i].grades[2], arr[i].grades[3],
               arr[i].avg_grade);
    }
}

/* ------------------------------------------------------------------ */
/*  Утилита: спросить у пользователя путь к файлу.                    */
/*  Если он просто нажмёт Enter — подставляется значение по умолчанию.*/
/* ------------------------------------------------------------------ */
static void ask_path(char *buf, size_t size, const char *def) {
    printf("Имя файла (Enter — %s): ", def);
    flush_line();   /* убираем '\n' после выбора пункта меню            */
    read_line(buf, size);
    if (buf[0] == '\0') {
        /* пользователь нажал Enter без ввода — берём значение по умолч. */
        strncpy(buf, def, size - 1);
        buf[size - 1] = '\0';
    }
}

/* ------------------------------------------------------------------ */
/*  Пункт 5 задания (часть 1): сохранение в текстовый файл (CSV).     */
/*                                                                    */
/*  Открываем файл на запись ("w"). Если он существует — будет        */
/*  перезаписан с нуля; если нет — создастся. fprintf пишет в файл    */
/*  так же, как printf — на экран.                                    */
/* ------------------------------------------------------------------ */
static void save_csv(const Student *arr, size_t n) {
    if (n == 0 || arr == NULL) {
        printf("Список пуст, сохранять нечего.\n");
        return;
    }

    char path[256];
    ask_path(path, sizeof(path), "data/students.csv");

    FILE *f = fopen(path, "w");
    if (f == NULL) {
        printf("Не удалось открыть файл '%s' на запись.\n", path);
        return;
    }

    /* Шапка — чтобы файл был самодокументирующимся.                    */
    fprintf(f, "surname,initials,group,g1,g2,g3,g4\n");

    for (size_t i = 0; i < n; ++i) {
        fprintf(f, "%s,%s,%d,%d,%d,%d,%d\n",
                arr[i].surname,
                arr[i].initials,
                arr[i].group,
                arr[i].grades[0], arr[i].grades[1],
                arr[i].grades[2], arr[i].grades[3]);
    }

    fclose(f);
    printf("Сохранено %zu студент(ов) в '%s'.\n", n, path);
}

/* ------------------------------------------------------------------ */
/*  Пункт 5 задания (часть 2): загрузка из CSV.                       */
/*                                                                    */
/*  Особенность задания: "после чтения массив должен автоматически    */
/*  расширяться/создаваться заново". Поэтому:                         */
/*    - старый массив освобождаем;                                    */
/*    - читаем файл по строкам через fgets;                            */
/*    - пропускаем первую строку (шапку);                             */
/*    - используем "растущий буфер": cap удваиваем при необходимости. */
/*                                                                    */
/*  Парсинг одной строки делаем через sscanf со спецификатором         */
/*  "%[^,]" — "читай всё, что не запятая". Это удобный способ          */
/*  разбирать CSV без сторонних библиотек.                            */
/* ------------------------------------------------------------------ */
static void load_csv(Student **arr, size_t *n) {
    char path[256];
    ask_path(path, sizeof(path), "data/students.csv");

    FILE *f = fopen(path, "r");
    if (f == NULL) {
        printf("Не удалось открыть файл '%s' на чтение.\n", path);
        return;
    }

    /* Освобождаем старый массив (задание просит "замещение").         */
    free(*arr);
    *arr = NULL;
    *n   = 0;

    /* Растущий буфер: cap — выделенная ёмкость, sz — фактическое      */
    /* количество элементов. Когда sz упрётся в cap — удвоим cap.       */
    size_t   cap  = 0;
    size_t   sz   = 0;
    Student *buf  = NULL;

    char line[512];
    int  first = 1;       /* флаг "сейчас первая строка — это шапка"   */

    while (fgets(line, sizeof(line), f) != NULL) {
        if (first) {
            first = 0;
            continue;     /* пропускаем строку с заголовками            */
        }

        /* Если нужно — увеличиваем ёмкость.                            */
        if (sz == cap) {
            size_t new_cap = (cap == 0) ? 4 : cap * 2;
            Student *tmp = realloc(buf, new_cap * sizeof(Student));
            if (tmp == NULL) {
                printf("Не удалось расширить буфер при чтении.\n");
                free(buf);
                fclose(f);
                return;
            }
            buf = tmp;
            cap = new_cap;
        }

        Student s;
        memset(&s, 0, sizeof(s));

        /* Парсим строку. %39[^,] = до запятой, максимум 39 символов     */
        /* (последний байт — для '\0'). %7[^,] — для коротких инициалов. */
        /* После каждого "%[...]" обязательно стоит запятая в формате,   */
        /* чтобы scanf "съел" разделитель.                               */
        int got = sscanf(line, "%39[^,],%7[^,],%d,%d,%d,%d,%d",
                         s.surname,
                         s.initials,
                         &s.group,
                         &s.grades[0], &s.grades[1],
                         &s.grades[2], &s.grades[3]);

        if (got != 7) {
            /* Битая строка — пропускаем с предупреждением, но не        */
            /* прекращаем чтение всего файла.                            */
            printf("Пропущена битая строка: %s", line);
            continue;
        }

        compute_avg(&s);
        buf[sz++] = s;
    }

    fclose(f);

    /* "Подрезаем" буфер до фактического размера, чтобы не держать      */
    /* лишнюю память.                                                   */
    if (sz > 0 && sz < cap) {
        Student *tmp = realloc(buf, sz * sizeof(Student));
        if (tmp != NULL) buf = tmp;
    }

    *arr = buf;
    *n   = sz;
    printf("Загружено студентов: %zu из '%s'.\n", sz, path);
}

/* ------------------------------------------------------------------ */
/*  Пункт 6 задания (часть 1): сохранение в бинарный файл.            */
/*                                                                    */
/*  Формат файла очень простой:                                       */
/*     [size_t n][Student #0][Student #1] ... [Student #n-1]          */
/*                                                                    */
/*  fwrite(ptr, size, count, file) пишет count объектов размера size  */
/*  по адресу ptr. Возвращает фактическое число записанных объектов.  */
/*                                                                    */
/*  Режим "wb" — write binary. На Windows важно отличать "w" от "wb": */
/*  при "w" символ '\n' автоматически превращается в "\r\n" — для     */
/*  бинарного потока это смертельно (испортит данные).                */
/* ------------------------------------------------------------------ */
static void save_bin(const Student *arr, size_t n) {
    if (n == 0 || arr == NULL) {
        printf("Список пуст, сохранять нечего.\n");
        return;
    }

    char path[256];
    ask_path(path, sizeof(path), "data/students.bin");

    FILE *f = fopen(path, "wb");
    if (f == NULL) {
        printf("Не удалось открыть файл '%s' на запись.\n", path);
        return;
    }

    /* Сначала пишем количество студентов одним size_t.                */
    if (fwrite(&n, sizeof(n), 1, f) != 1) {
        printf("Ошибка записи заголовка.\n");
        fclose(f);
        return;
    }

    /* Затем — весь массив одним вызовом. n структур по sizeof(Student).*/
    size_t written = fwrite(arr, sizeof(Student), n, f);
    fclose(f);

    if (written != n) {
        printf("Ошибка: записано %zu из %zu.\n", written, n);
        return;
    }

    /* Подскажем пользователю размер файла, чтобы он мог сравнить с CSV.*/
    size_t bytes = sizeof(n) + n * sizeof(Student);
    printf("Сохранено %zu студент(ов) в '%s' (%zu байт).\n",
           n, path, bytes);
}

/* ------------------------------------------------------------------ */
/*  Пункт 6 задания (часть 2): чтение из бинарного файла.             */
/*                                                                    */
/*  Алгоритм:                                                         */
/*    1) открыть "rb";                                                */
/*    2) прочитать size_t — это будет количество студентов;           */
/*    3) выделить под массив ровно столько памяти, сколько нужно;     */
/*    4) одним fread прочитать всё содержимое в этот массив.          */
/* ------------------------------------------------------------------ */
static void load_bin(Student **arr, size_t *n) {
    char path[256];
    ask_path(path, sizeof(path), "data/students.bin");

    FILE *f = fopen(path, "rb");
    if (f == NULL) {
        printf("Не удалось открыть файл '%s' на чтение.\n", path);
        return;
    }

    size_t count = 0;
    if (fread(&count, sizeof(count), 1, f) != 1) {
        printf("Ошибка чтения заголовка (файл пустой или повреждён).\n");
        fclose(f);
        return;
    }

    /* Освобождаем то, что было раньше — задание требует "восстановления".*/
    free(*arr);
    *arr = NULL;
    *n   = 0;

    if (count == 0) {
        fclose(f);
        printf("Файл содержит 0 студентов.\n");
        return;
    }

    Student *buf = malloc(count * sizeof(Student));
    if (buf == NULL) {
        printf("Не удалось выделить память на %zu студентов.\n", count);
        fclose(f);
        return;
    }

    size_t got = fread(buf, sizeof(Student), count, f);
    fclose(f);

    if (got != count) {
        printf("Ошибка: ожидалось %zu, прочитано %zu. Данные могут быть неполными.\n",
               count, got);
        /* Что делать с частично прочитанным — на выбор; здесь оставим  */
        /* только то, что реально удалось прочитать.                    */
        count = got;
        if (count == 0) {
            free(buf);
            return;
        }
        Student *tmp = realloc(buf, count * sizeof(Student));
        if (tmp != NULL) buf = tmp;
    }

    *arr = buf;
    *n   = count;
    printf("Загружено студентов: %zu из '%s'.\n", count, path);
}

/* ------------------------------------------------------------------ */
/*  Пункт 4 задания: поиск всех студентов из заданной группы.         */
/*                                                                    */
/*  Просто линейный проход по массиву. Сложность O(n), для учебной    */
/*  задачи нормально. Печатаем шапку, а затем все найденные строки.   */
/* ------------------------------------------------------------------ */
static void find_by_group(const Student *arr, size_t n) {
    if (n == 0 || arr == NULL) {
        printf("Список пуст. Сначала введите или загрузите данные.\n");
        return;
    }

    int group = 0;
    printf("Введите номер группы для поиска: ");
    if (!read_int(&group)) {
        printf("Ошибка ввода.\n");
        return;
    }

    size_t found = 0;
    printf("\n%-3s %-20s %-8s %-7s %-15s %-9s\n",
           "№", "Фамилия", "И.О.", "Группа", "Оценки", "Сред.балл");
    printf("--------------------------------------------------------------------\n");

    for (size_t i = 0; i < n; ++i) {
        if (arr[i].group == group) {
            printf("%-3zu %-20s %-8s %-7d %d %d %d %d   %6.2f\n",
                   i + 1,
                   arr[i].surname,
                   arr[i].initials,
                   arr[i].group,
                   arr[i].grades[0], arr[i].grades[1],
                   arr[i].grades[2], arr[i].grades[3],
                   arr[i].avg_grade);
            ++found;
        }
    }

    if (found == 0) {
        printf("Студенты группы %d не найдены.\n", group);
    } else {
        printf("--------------------------------------------------------------------\n");
        printf("Найдено: %zu студент(ов) в группе %d.\n", found, group);
    }
}

/* ------------------------------------------------------------------ */
/*  Пункт 3 задания: сортировка через qsort из <stdlib.h>.            */
/*                                                                    */
/*  qsort требует, чтобы компаратор имел сигнатуру:                   */
/*     int cmp(const void *a, const void *b);                         */
/*                                                                    */
/*  Возвращаемое значение:                                            */
/*     <0  если *a должен идти раньше *b,                             */
/*     >0  если *a должен идти позже *b,                              */
/*     0   если они "равны" для целей сортировки.                     */
/*                                                                    */
/*  void* — потому что qsort универсален и не знает тип элементов.    */
/*  Внутри компаратора мы приводим указатели к (const Student *).     */
/* ------------------------------------------------------------------ */

/* Сравнение по фамилии: лексикографически, через strcmp.              */
static int cmp_by_surname(const void *a, const void *b) {
    const Student *sa = (const Student *)a;
    const Student *sb = (const Student *)b;
    return strcmp(sa->surname, sb->surname);
}

/* Сравнение по номеру группы. Просто разность двух int, но напрямую   */
/* (a - b) делать нельзя: при больших значениях int может переполниться. */
/* Поэтому пишем явные условия.                                        */
static int cmp_by_group(const void *a, const void *b) {
    const Student *sa = (const Student *)a;
    const Student *sb = (const Student *)b;
    if (sa->group < sb->group) return -1;
    if (sa->group > sb->group) return  1;
    return 0;
}

/* Сравнение по среднему баллу. То же самое, но для double.            */
/* По убыванию: лучшие (с большим баллом) должны идти первыми.         */
static int cmp_by_avg_desc(const void *a, const void *b) {
    const Student *sa = (const Student *)a;
    const Student *sb = (const Student *)b;
    if (sa->avg_grade > sb->avg_grade) return -1;
    if (sa->avg_grade < sb->avg_grade) return  1;
    return 0;
}

/* Обёртка: спрашивает у пользователя, по какому полю сортировать,    */
/* и вызывает qsort с подходящим компаратором.                         */
static void sort_students(Student *arr, size_t n) {
    if (n < 2 || arr == NULL) {
        printf("Нечего сортировать (студентов меньше двух).\n");
        return;
    }

    printf("\nСортировать по:\n");
    printf("  1 — фамилии (по алфавиту)\n");
    printf("  2 — номеру группы (по возрастанию)\n");
    printf("  3 — среднему баллу (по убыванию)\n");
    printf("Выбор: ");

    int key = 0;
    if (!read_int(&key)) {
        printf("Ошибка ввода.\n");
        return;
    }

    /* qsort(база, число_элементов, размер_элемента, компаратор).       */
    switch (key) {
        case 1: qsort(arr, n, sizeof(Student), cmp_by_surname);  break;
        case 2: qsort(arr, n, sizeof(Student), cmp_by_group);    break;
        case 3: qsort(arr, n, sizeof(Student), cmp_by_avg_desc); break;
        default:
            printf("Неизвестный ключ сортировки.\n");
            return;
    }

    printf("Готово. Используйте пункт 2, чтобы посмотреть таблицу.\n");
}

/* ------------------------------------------------------------------ */
/*  Пункт 7 задания (часть 1): добавить ОДНОГО студента в конец массива.*/
/*                                                                    */
/*  Используем realloc, чтобы РАСШИРИТЬ существующий блок памяти на   */
/*  один элемент. realloc делает три вещи:                            */
/*    - пытается увеличить блок на месте;                             */
/*    - если не получается — выделяет новый блок, копирует в него     */
/*      старые данные и освобождает старый;                           */
/*    - возвращает указатель на полученный блок (может отличаться от  */
/*      исходного!).                                                  */
/*                                                                    */
/*  Важная ловушка: НЕЛЬЗЯ писать "*arr = realloc(*arr, ...)" напрямую*/
/*  без промежуточной переменной. Если realloc вернёт NULL, мы        */
/*  потеряем исходный указатель и не сможем освободить старую память. */
/*  Поэтому пишем во временную переменную tmp и присваиваем только    */
/*  после проверки.                                                   */
/* ------------------------------------------------------------------ */
static void add_student(Student **arr, size_t *n) {
    Student s;
    /* Зануляем структуру целиком (на случай, если строки окажутся     */
    /* короче буферов — остаток будет нулевыми байтами).               */
    memset(&s, 0, sizeof(s));

    printf("\n--- Новый студент ---\n");

    printf("Фамилия: ");
    flush_line();   /* убираем '\n', оставшийся после выбора пункта 9  */
    read_line(s.surname, sizeof(s.surname));

    printf("Инициалы (например, А.Б.): ");
    read_line(s.initials, sizeof(s.initials));

    printf("Номер группы: ");
    if (!read_int(&s.group)) {
        printf("Ошибка ввода группы, ставим 0.\n");
        s.group = 0;
    }

    printf("Оценки (4 числа через пробел): ");
    for (int k = 0; k < 4; ++k) {
        if (!read_int(&s.grades[k])) {
            printf("Ошибка ввода оценки №%d, ставим 0.\n", k + 1);
            s.grades[k] = 0;
        }
    }
    flush_line();
    compute_avg(&s);

    /* Расширяем массив на одну ячейку.                                */
    Student *tmp = realloc(*arr, (*n + 1) * sizeof(Student));
    if (tmp == NULL) {
        printf("Не удалось расширить массив (realloc вернул NULL).\n");
        return;   /* *arr и *n остались валидными, ничего не теряем   */
    }
    *arr = tmp;

    /* Записываем нового студента в самый конец и увеличиваем размер.  */
    (*arr)[*n] = s;
    *n += 1;

    printf("Добавлен. Всего студентов: %zu\n", *n);
}

/* ------------------------------------------------------------------ */
/*  Пункт 7 задания (часть 2): удалить студента по индексу.           */
/*                                                                    */
/*  Алгоритм:                                                         */
/*    1) Спросить у пользователя номер (1..n) удаляемого студента.    */
/*    2) Сдвинуть все элементы, стоящие правее, на одну позицию влево */
/*       при помощи memmove (он корректно работает при перекрытии).   */
/*    3) Сжать массив до n-1 элементов через realloc.                 */
/*                                                                    */
/*  Особый случай: если удаляем последний оставшийся элемент,         */
/*  realloc на 0 элементов поведёт себя странно (в стандарте C — UB), */
/*  поэтому вместо realloc(0) делаем free + NULL.                     */
/* ------------------------------------------------------------------ */
static void remove_student(Student **arr, size_t *n) {
    if (*n == 0 || *arr == NULL) {
        printf("Список пуст — удалять нечего.\n");
        return;
    }

    printf("\nТекущий список:\n");
    print_table(*arr, *n);

    int idx = 0;
    printf("\nНомер записи для удаления (1..%zu): ", *n);
    if (!read_int(&idx)) {
        printf("Ошибка ввода.\n");
        return;
    }
    flush_line();

    if (idx < 1 || (size_t)idx > *n) {
        printf("Номер вне диапазона.\n");
        return;
    }

    /* Переводим из "номера для пользователя" (1..n) в индекс массива (0..n-1). */
    size_t i = (size_t)idx - 1;

    /* Сдвиг "влево" всех элементов, стоящих после i. memmove(dst, src, bytes) */
    /* безопасен при перекрытии (в отличие от memcpy).                          */
    /* Количество сдвигаемых элементов = (n - i - 1).                           */
    if (i < *n - 1) {
        memmove(&(*arr)[i], &(*arr)[i + 1],
                (*n - i - 1) * sizeof(Student));
    }

    *n -= 1;

    /* Сжимаем буфер. Если массив опустел — освобождаем целиком.       */
    if (*n == 0) {
        free(*arr);
        *arr = NULL;
    } else {
        Student *tmp = realloc(*arr, *n * sizeof(Student));
        /* Тут важный момент: даже если realloc вернул NULL (что для   */
        /* сжатия крайне маловероятно), наш старый блок ОСТАЁТСЯ       */
        /* валидным и содержит уже сдвинутые данные — это допустимо,   */
        /* просто буфер чуть больше, чем нужно. Поэтому ругаться не на */
        /* что: данные не потеряны.                                    */
        if (tmp != NULL) *arr = tmp;
    }

    printf("Удалено. Осталось студентов: %zu\n", *n);
}

